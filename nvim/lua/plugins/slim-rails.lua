-- essential parts without becoming cryptic. It started as an exercise to see
-- how much could be removed from a standard html template (<, >, closing tags,
-- etc...). As more people took an interest in Slim, the functionality grew and
-- so did the flexibility of the syntax. See https://github.com/slim-template/slim
-- for more details.

